// 자연수 n이 매개변수
// 순서쌍의 갯수를 return
// 약수의 개수를 구하는 수학적 특성을 이용
// 약수의 특성 중 하나는 약수의 쌍은 대칭적


function solution(n) {
    let count = 0;
    // 약수는 해당 숫자의 절반까지만 확인하면 됨
    for (let i = 1; i <= Math.sqrt(n); i++) {
        // i가 n의 약수인 경우
        if (n % i === 0) {
            // i와 n/i가 서로 다른 약수 쌍을 형성함
            // 예를 들어, n이 36일 때, (1, 36), (2, 18), (3, 12), (4, 9)가 약수 쌍을 형성함
            // 제곱수인 경우에는 중복이 발생하지 않으므로 1개만 추가
            count += (i * i === n) ? 1 : 2;
        }
    }
    return count; // 약수 쌍의 개수 반환
}
